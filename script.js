// ===== Popup =====
const popup = document.getElementById('letterPopup');
const openLetter = document.getElementById('openLetter');
const paperSound = document.getElementById('paperSound');
const hero = document.getElementById('home');

openLetter.addEventListener('click', () => {
  paperSound.currentTime = 0; // reset เสียงทุกครั้ง
  paperSound.play();          // เล่นเสียงกระดาษเปิดจดหมาย
  popup.classList.add('fade-out');

  setTimeout(() => {
    popup.style.display = 'none';
    hero.classList.remove('hidden');
    hero.classList.add('fade-up');
  }, 800);
});

// ===== Fade-up Animation =====
const fadeEls = document.querySelectorAll(".fade-up");

function fadeUpOnScroll() {
  fadeEls.forEach(el => {
    const rect = el.getBoundingClientRect();
    if (rect.top < window.innerHeight - 80) {
      el.classList.add("show");
    }
  });
}

window.addEventListener("scroll", fadeUpOnScroll);
window.addEventListener("load", fadeUpOnScroll);

