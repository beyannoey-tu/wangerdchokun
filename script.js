// === Render ===
const reasonList = document.getElementById('reasonList');
const prevBtn = document.getElementById('prevBtn');
const nextBtn = document.getElementById('nextBtn');
const pageInfo = document.getElementById('pageInfo');

let currentPage = 0;
const perPage = 20;

function renderReasons() {
  reasonList.innerHTML = '';

  const start = currentPage * perPage;
  const end = Math.min(start + perPage, reasons.length);

  for (let i = start; i < end; i++) {
    const li = document.createElement('li');
    li.textContent = reasons[i];
    li.classList.add('fade-up');
    reasonList.appendChild(li);
  }

  pageInfo.textContent = `Page ${currentPage + 1} / ${Math.ceil(reasons.length / perPage)}`;
  fadeUpOnScroll();
}

nextBtn.addEventListener('click', () => {
  if ((currentPage + 1) * perPage < reasons.length) {
    currentPage++;
    renderReasons();
    window.scrollTo({ top: reasonList.offsetTop - 80, behavior: 'smooth' });
  }
});

prevBtn.addEventListener('click', () => {
  if (currentPage > 0) {
    currentPage--;
    renderReasons();
    window.scrollTo({ top: reasonList.offsetTop - 80, behavior: 'smooth' });
  }
});

window.addEventListener('load', renderReasons);